from enum import Enum

# Los colores los definiremos como si fueran un enumerado
class Color(Enum):
    ACCENT = "#EA5940" # rojo
    PRIMARY = "#212529" # gris oscuro
    SECONDARY = "#D3D3D3" #gris claro
    
class TextColor(Enum):
    ACCENT = "#EA5940"
    PRIMARY = "#FFFFFF"
    SECONDARY = "#212529"
    TERTIARY = "#D3D3D3"